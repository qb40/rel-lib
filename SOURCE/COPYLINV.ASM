RELCOPYLINEV PROC

MOV BX,BP
MOV DX,DS

MOV BP,SP
XOR DI,DI
MOV ES,[BP+8]
MOV DS,[BP+6]
MOV AX,[BP+4]
ADD DI,AX
MOV SI,DI
MOV CX,200
LBL_LINE_LOOP:
        MOV AL,DS:[SI]
        MOV ES:[DI],AL
        ADD DI,320
        ADD SI,320
        DEC CX
JNZ LBL_LINE_LOOP

MOV BP,BX
MOV DS,DX

RET 10

RELCOPYLINEV ENDP
END
